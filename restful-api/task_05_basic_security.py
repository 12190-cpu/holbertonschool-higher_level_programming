# task_05_basic_security.py

from flask import Flask, jsonify, request
from flask_httpauth import HTTPBasicAuth
from werkzeug.security import generate_password_hash, check_password_hash
from flask_jwt_extended import (
    JWTManager, create_access_token,
    jwt_required, get_jwt_identity, get_jwt
)

app = Flask(__name__)

# =========================
# CONFIGURATION
# =========================
app.config["JWT_SECRET_KEY"] = "super-secret-key-change-this"

auth = HTTPBasicAuth()
jwt = JWTManager(app)

# =========================
# UTILISATEURS EN MÉMOIRE
# =========================
users = {
    "user1": {
        "username": "user1",
        "password": generate_password_hash("password"),
        "role": "user"
    },
    "admin1": {
        "username": "admin1",
        "password": generate_password_hash("password"),
        "role": "admin"
    }
}

# =========================
# AUTHENTIFICATION BASIQUE
# =========================
@auth.verify_password
def verify_password(username, password):
    """Vérifie le couple (username, password) pour l'authentification basique."""
    user = users.get(username)
    if not user:
        return False
    return check_password_hash(user["password"], password)


@app.route("/basic-protected")
@auth.login_required
def basic_protected():
    """Route protégée par Basic Auth"""
    return "Basic Auth: Access Granted"


# =========================
# AUTHENTIFICATION JWT
# =========================
@app.route("/login", methods=["POST"])
def login():
    """Génère un token JWT si les identifiants sont valides"""
    data = request.get_json()

    if not data or "username" not in data or "password" not in data:
        return jsonify({"error": "Invalid credentials"}), 401

    username = data["username"]
    password = data["password"]

    user = users.get(username)
    if not user or not check_password_hash(user["password"], password):
        return jsonify({"error": "Invalid credentials"}), 401

    # Créer un JWT avec identité et rôle
    token = create_access_token(identity=username, additional_claims={"role": user["role"]})
    return jsonify({"access_token": token}), 200


@app.route("/jwt-protected")
@jwt_required()
def jwt_protected():
    """Route protégée par JWT"""
    return "JWT Auth: Access Granted"


@app.route("/admin-only")
@jwt_required()
def admin_only():
    """Accès réservé aux administrateurs"""
    current_user = get_jwt_identity()
    claims = get_jwt()

    if claims.get("role") != "admin":
        return jsonify({"error": "Admin access required"}), 403

    return "Admin Access: Granted"


# =========================
# GESTION DES ERREURS JWT
# =========================
@jwt.unauthorized_loader
def handle_unauthorized_error(err):
    return jsonify({"error": "Missing or invalid token"}), 401


@jwt.invalid_token_loader
def handle_invalid_token_error(err):
    return jsonify({"error": "Invalid token"}), 401


@jwt.expired_token_loader
def handle_expired_token_error(jwt_header, jwt_payload):
    return jsonify({"error": "Token has expired"}), 401


@jwt.revoked_token_loader
def handle_revoked_token_error(jwt_header, jwt_payload):
    return jsonify({"error": "Token has been revoked"}), 401


@jwt.needs_fresh_token_loader
def handle_needs_fresh_token_error(jwt_header, jwt_payload):
    return jsonify({"error": "Fresh token required"}), 401


# =========================
# DÉMARRAGE DU SERVEUR
# =========================
if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5000)
