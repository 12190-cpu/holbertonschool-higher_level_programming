"""
Module 5-number_keys
This module defines a function that returns the number of keys in a dictionary.
"""


def number_keys(a_dictionary):
    """
    Returns the number of keys in a dictionary.

    Args:
        a_dictionary (dict): The dictionary to analyze.

    Returns:
        int: The number of keys in the dictionary.
    """
    return len(a_dictionary)
